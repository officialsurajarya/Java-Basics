package DataTypes;

public class Variable {
    static void main(String[] args) {
        int num = 67;
        System.out.println(num);
        char ch = 's';
        System.out.println(ch);
        float f = 5.6f;
        System.out.println("float: "+f);

        boolean b = true;
        System.out.println(b);
    }
}
