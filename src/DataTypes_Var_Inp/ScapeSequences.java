package DataTypes_Var_Inp;

public class ScapeSequences {
    public static void main(String[] args) {
        System.out.println("Suraj\tArya");
        System.out.println("Suraj\bArya");
        System.out.println("Suraj\nArya");
        System.out.println("Suraj\'Arya");
        System.out.println("Suraj\"Arya");
        System.out.println("Suraj\\Arya");

        System.out.println("*\n* *\n* * *\n* * * *\n* * * * *");
    }
}
